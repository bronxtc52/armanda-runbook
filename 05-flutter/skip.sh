#!/usr/bin/env bash
# ============================================================================
# skip.sh — «кнопка отказа»: человек решил НЕ ставить Flutter (фаза 05).
#
# ЧТО ЭТО: фаза 05 (Flutter + Xcode + Android Studio + CocoaPods) — по желанию.
#   Гид предлагает её перед стартом; если человеку не нужна мобильная
#   разработка — запускается этот скрипт. Он честно фиксирует пропуск.
# ЧТО МЕНЯЕТ НА МАШИНЕ: только пишет маркеры в state/progress.log.
#   Ничего не устанавливает и не удаляет.
# НУЖЕН ЛИ ПАРОЛЬ/SUDO: нет.   СКОЛЬКО ВРЕМЕНИ: мгновенно.
#
# ВАЖНО: вместе с 05 пропускается и фаза 06 «Первая победа» — она целиком
# построена на Flutter (приложение-счётчик на симуляторе). Чек-поинт 07
# это учитывает и считает укороченный чек-лист.
# ============================================================================
set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "$SCRIPT_DIR/../scripts/lib.sh"

step "Фаза 05 (Flutter) — пропускаем по твоему выбору"

note "Flutter, Xcode и Android Studio нужны для МОБИЛЬНЫХ приложений."
note "Раз они тебе сейчас не нужны — ставить их не будем. Это нормально:"
note "все остальные инструменты курса уже работают без них."

mark_step "05-flutter:skipped"
mark_step "06-first-win:skipped"

ok "Записал: фаза 05 пропущена по выбору."
warn "Вместе с ней пропускается и фаза 06 «Первая победа» — она про запуск"
note "приложения на симуляторе iPhone и без Flutter невозможна."
note "Если позже передумаешь — просто вернись и запусти скрипты фазы 05:"
note "маркер пропуска не мешает установке, а чек-поинт пересчитается сам."

ok "Дальше — веб-трек (05w-netlify, сайты), если выбран, или сразу фаза 07: самопроверка."
