#!/bin/sh
# ============================================================================
# askpass.sh — SUDO_ASKPASS-хелпер: нативное окно macOS для пароля от Mac (sudo).
#
# ЧТО ЭТО: sudo вызывает эту программу, когда ему нужен пароль (флаг -A). Она
# показывает стандартное окно macOS (osascript) и печатает введённый пароль
# в stdout — оттуда его читает сам sudo.
#
# БЕЗОПАСНОСТЬ (критично):
#   • пароль печатается ТОЛЬКО в stdout, который читает sudo отдельным процессом
#     через пайп — он НЕ попадает в вывод раннера (PTY), в лог, в файл, в argv;
#   • stderr гасим, чтобы ничего лишнего не утекло в поток;
#   • отмена диалога → osascript выходит с ненулевым кодом → sudo корректно
#     прекращает попытку (никакого зависания, никакого пустого пароля наугад).
#
# Аргумент от sudo ($1 = "[sudo] password for …") НАМЕРЕННО игнорируем — свой
# текст без интерполяции, чтобы исключить любую shell-инъекцию.
# ============================================================================
exec /usr/bin/osascript \
  -e 'display dialog "Установщику нужен пароль от вашего Mac, чтобы выполнить системную настройку (sudo). Это стандартное окно macOS — то же, что просит пароль при установке программ." with title "Пароль администратора" default answer "" with hidden answer with icon caution' \
  -e 'text returned of result' \
  2>/dev/null
